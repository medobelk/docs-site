метод лечения, направленный на
удаление из крови различных
токсических продуктов и регуляцию 
гемостаза путем контакта крови с
сорбентом вне организма. Метод 
основан на двух свойствах сорбента: 
адсорбции (фиксация молекулы
вещества на поверхности 
поглотителя); абсорбции (фиксация
вещества в объеме поглотителя). 
Фиксация химических агентов 
происходит за счет образования 
ковалентных или ионных связей 
вещества с активными группами 
поглотителя.
